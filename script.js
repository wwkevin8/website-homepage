const footerYear = document.querySelector("#footer-year");
const metaDescription = document.querySelector('meta[name="description"]');
const langButtons = Array.from(document.querySelectorAll(".lang-btn"));
const i18nNodes = Array.from(document.querySelectorAll("[data-i18n]"));

function applyLanguage(lang) {
  const dict = translations[lang] || translations["zh-CN"];
  const page = getPageKey();
  const meta = getMetaContent(page, lang);

  document.documentElement.lang = dict.htmlLang;
  document.title = meta.title;

  if (metaDescription) {
    metaDescription.setAttribute("content", meta.description);
  }

  i18nNodes.forEach(node => {
    const key = node.dataset.i18n;
    if (dict[key]) {
      node.textContent = dict[key];
    }
  });

  if (footerYear) {
    footerYear.textContent = dict.footerYear(new Date().getFullYear());
  }

  langButtons.forEach(button => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
  });

  window.localStorage.setItem("ngn-lang", lang);
  initStorageCalculator(lang);
  initPickupQuoteForm(lang);
  initPickupBoard(lang);
  initPickupBoardAdmin(lang);
  initHomeVideos(lang);
  initHomeMenu();
  initTeamSummaryCarousel(lang);
}

langButtons.forEach(button => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.lang);
  });
});

const savedLanguage = window.localStorage.getItem("ngn-lang") || "zh-CN";
applyLanguage(savedLanguage);
