function initHomeVideos(activeLang) {
  const videoCards = Array.from(document.querySelectorAll("[data-video-card]"));
  if (!videoCards.length) {
    return;
  }

  function pauseAll() {
    videoCards.forEach(card => {
      const video = card.querySelector("video");
      const label = card.querySelector(".video-play-label");
      card.classList.remove("is-playing");
      if (video) {
        video.pause();
      }
      if (label) {
        label.textContent = translations[activeLang].videoPlay;
      }
    });
  }

  if (!document.body.dataset.homeVideosBound) {
    videoCards.forEach(card => {
      card.addEventListener("click", () => {
        const video = card.querySelector("video");
        const label = card.querySelector(".video-play-label");
        if (!video) {
          return;
        }

        const isPlaying = !video.paused && !video.ended;
        pauseAll();

        if (!isPlaying) {
          video.play().catch(() => {});
          card.classList.add("is-playing");
          if (label) {
            label.textContent = translations[document.documentElement.lang === "en" ? "en" : "zh-CN"].videoPause;
          }
        }
      });

      const video = card.querySelector("video");
      if (video) {
        video.addEventListener("pause", () => {
          card.classList.remove("is-playing");
          const label = card.querySelector(".video-play-label");
          if (label) {
            label.textContent = translations[document.documentElement.lang === "en" ? "en" : "zh-CN"].videoPlay;
          }
        });
      }
    });

    document.body.dataset.homeVideosBound = "true";
  }

  videoCards.forEach(card => {
    const label = card.querySelector(".video-play-label");
    const video = card.querySelector("video");
    if (label) {
      label.textContent = card.classList.contains("is-playing")
        ? translations[activeLang].videoPause
        : translations[activeLang].videoPlay;
    }
  });
}

function initHomeMenu() {
  const menuPanel = document.querySelector("#home-menu-panel");
  const toggleButtons = Array.from(document.querySelectorAll("[data-menu-toggle]"));
  const menuLinks = Array.from(document.querySelectorAll("[data-menu-link]"));

  if (!menuPanel || !toggleButtons.length) {
    return;
  }

  if (document.body.dataset.homeMenuBound === "true") {
    return;
  }

  const closeMenu = () => {
    menuPanel.hidden = true;
    toggleButtons.forEach(button => {
      button.setAttribute("aria-expanded", "false");
    });
  };

  const openMenu = () => {
    menuPanel.hidden = false;
    toggleButtons.forEach(button => {
      button.setAttribute("aria-expanded", "true");
    });
  };

  toggleButtons.forEach(button => {
    button.setAttribute("aria-controls", "home-menu-panel");
    button.setAttribute("aria-expanded", "false");
    button.addEventListener("click", event => {
      event.preventDefault();
      if (menuPanel.hidden) {
        openMenu();
      } else {
        closeMenu();
      }
    });
  });

  menuLinks.forEach(link => {
    link.addEventListener("click", event => {
      const targetId = link.getAttribute("href");
      if (!targetId) {
        return;
      }

      closeMenu();

      if (targetId.startsWith("#")) {
        event.preventDefault();
        const target = document.querySelector(targetId);
        if (target) {
          target.scrollIntoView({ behavior: "smooth", block: "start" });
        }
        return;
      }

      if (targetId) {
        event.preventDefault();
        window.location.href = targetId;
      }
    });
  });

  document.addEventListener("click", event => {
    const target = event.target;
    if (!(target instanceof Element)) {
      return;
    }

    const clickedToggle = target.closest("[data-menu-toggle]");
    const clickedInsidePanel = target.closest("#home-menu-panel");

    if (!clickedToggle && !clickedInsidePanel && !menuPanel.hidden) {
      closeMenu();
    }
  });

  document.addEventListener("keydown", event => {
    if (event.key === "Escape" && !menuPanel.hidden) {
      closeMenu();
    }
  });

  document.body.dataset.homeMenuBound = "true";
}

function getTeamSummaryShortText(text, lang) {
  if (!text) {
    return "";
  }

  if (lang === "en") {
    const sentence = text.split(". ").find(Boolean);
    if (sentence) {
      return sentence.endsWith(".") ? sentence : `${sentence}.`;
    }
  } else {
    const sentence = text.split("。").find(Boolean);
    if (sentence) {
      return `${sentence}。`;
    }
  }

  return text.length > 88 ? `${text.slice(0, 88).trim()}...` : text;
}

